function replacetext() {
    $("[sh_lang]").each(function (index, obj) {
        var langID = $(obj).attr('sh_lang');
        var txt = eval('(' + langID + ')');
        var ntxt;
        if ($(obj).is('textarea') || $(obj).attr('type') == 'text' || $(obj).attr('type') == 'password') {
            $(obj).attr('placeholder', txt).addClass('placeholder');
            return;
        } else if ($(obj).html() != '' && typeof($(obj).attr('insert')) != 'undefined') {
            ntxt = txt + $(obj).html();
            $(obj).empty().html(ntxt);
            return;
        } else {
            $(obj).html(txt);
        }
    });
    $("[sh_title]").each(function (index, obj) {
        var langID = $(obj).attr('sh_title');
        var txt = eval('(' + langID + ')');
        $(obj).attr('title', txt);
    });
    //reqtip();
}
